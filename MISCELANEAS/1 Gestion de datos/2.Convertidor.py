celsius = float(input("Ingrese la cantidad de grados celsius a convertir: ") )
fahrenheit = 9/5 * celsius + 32
kelvin = celsius + 273.15
rankine = celsius*9/5 + 491.67
print(celsius,' grados celsius equivale a ',fahrenheit,' grados fahrenheit.')
print(celsius,' grados celsius equivale a ',kelvin,' grados kelvin.')
print(celsius,' grados celsius equivale a ',rankine,' grados rankine.')