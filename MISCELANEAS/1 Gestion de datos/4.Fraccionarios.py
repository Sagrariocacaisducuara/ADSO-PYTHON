from fractions import Fraction
f1=Fraction(input('Introduce un numero fraccionario, ejemplo 2/4: '))
f2=Fraction(input('Introduce un numero fraccionario, ejemplo 4/8: '))
print('El resultado de la suma es: ', str(f1+f2))
print('El resultado de la resta es: ', str(f1-f2))
print('El resultado de la multiplicacion es: ', str(f1*f2))
print('El resultado de la division es: ', str(f1/f2))